namespace LemonUI.Phone
{
    /// <summary>
    /// The behavior of a call after is initiated by the user.
    /// </summary>
    public enum CallBehavior
    {
        /// <summary>
        /// The contact being called is busy.
        /// </summary>
        Busy = 0,
        /// <summary>
        /// The contact is available and will answer.
        /// </summary>
        Available = 1
    }
}
